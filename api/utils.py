def checkGenre(genre):
        if(genre == 'sertanejo'):
            return 'Country'
        elif(genre == 'pop'):
            return 'Pop'
        elif(genre == 'gospel'):
            return 'R & B'
        elif(genre == 'mpb'):
            return 'Latin'
        elif(genre == 'samba-pagode'):
            return 'Latin'
        elif(genre == 'rock-internacional'):
            return 'Rock'
        elif(genre == 'forro-piseiro'):
            return 'Country'
        elif(genre == 'eletronica'):
            return 'Dance/Electronic'
        elif(genre == 'rock-brasil'):
            return 'Rock'
        elif(genre == 'hip-hop-rap'):
            return 'Hip Hop'
        
